# Tipe data numerik
integer_number = 11
float_number = 2.23
complex_number = 1 + 5j

# Tipe data teks
string_data = "Hello, World!"

# Tipe data boolean
boolean_data = True  # atau False

# Tipe data list (daftar)
list_data = [1, 2, 3, 'a', 'b', 'c']

# Tipe data tuple
tuple_data = (4, 5, 6, 'x', 'y', 'z')

# Tipe data set (himpunan)
set_data = {7, 8, 9, 'apple', 'banana', 'cherry'}

# Tipe data dictionary
dictionary_data = {'name': 'apey', 'age': 21, 'city': 'cobodas'}

# Menampilkan nilai dari masing-masing tipe data
print("Nilai dari tipe data numerik:")
print("Integer Number:", integer_number)
print("Float Number:", float_number)
print("Complex Number:", complex_number)

print("\nNilai dari tipe data teks:")
print("String Data:", string_data)

print("\nNilai dari tipe data boolean:")
print("Boolean Data:", boolean_data)

print("\nNilai dari tipe data list:")
print("List Data:", list_data)

print("\nNilai dari tipe data tuple:")
print("Tuple Data:", tuple_data)

print("\nNilai dari tipe data set:")
print("Set Data:", set_data)

print("\nNilai dari tipe data dictionary:")
print("Dictionary Data:", dictionary_data)
