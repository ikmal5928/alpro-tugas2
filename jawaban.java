
public class Jawaban {

    public static void main(String[] args) {
        // Tipe data numerik
        int nilaiInt = 9;
        long nilaiLong = 4000000L;
        float nilaiFloat = 2.31f;
        double nilaiDouble = 2.71828;

        // Tipe data karakter
        char karakter = 'A';

        // Tipe data boolean
        boolean nilaiBoolean = true;

        // Tipe data string
        String teks = "Halo, ini contoh string.";

        // Menampilkan nilai dari tipe data
        System.out.println("Nilai dari tipe data int: " + nilaiInt);
        System.out.println("Nilai dari tipe data long: " + nilaiLong);
        System.out.println("Nilai dari tipe data float: " + nilaiFloat);
        System.out.println("Nilai dari tipe data double: " + nilaiDouble);
        System.out.println("Nilai dari tipe data char: " + karakter);
        System.out.println("Nilai dari tipe data boolean: " + nilaiBoolean);
        System.out.println("Nilai dari tipe data string: " + teks);
    }
}